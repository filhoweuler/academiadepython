#criar uma lista
lista = []

#criar uma lista com valores
lista = [ 2 , 5 , 8 , 1 ]

#acessar um valor
lista[3]

#alterar um valor
lista[3] = 25

#copiar de uma posicao pra outra
lista[3] = lista[1]

#imprimir lista inteira
print(lista)

#descobrir o tamanho da lista
len(lista)

#remover um elemento da lista
del lista[0]

#indexação
print(lista[0])

#indices negativos
print(lista[-1])

#inserir no final da lista
lista.append(100)

#inserir elemento no meio da lista
lista.insert(1, 99)